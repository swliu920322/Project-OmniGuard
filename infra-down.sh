#!/bin/bash
set -e

# =========================================================================
# 🛑 Project-OmniGuard: 计算挂起与 FinOps 财务止损脚本
# =========================================================================

PREFIX="omni"
RG="${PREFIX}-guard-infra-rg"

echo "========================================================="
echo "🛑 [Step 1/2] 正在向计算宿主计划投递窒息信号 (Stop Function)..."
echo "========================================================="
APP_NAME=$(az webapp list --resource-group "$RG" --query "[?kind=='functionapp,linux'].name" -o tsv)

if [ -z "$APP_NAME" ]; then
    echo "⚠️ 警告: 未在资源组 $RG 中发现活跃的 Function App，跳过停止动作。"
else
    az webapp stop --name "$APP_NAME" --resource-group "$RG"
    echo "🟥 计算大脑已成功挂起。状态：Stopped (B1 计时费已成功截断)"
fi

echo -e "\n========================================================="
echo "📊 [Step 2/2] Project-OmniGuard 财务风险对账底盘"
echo "========================================================="
echo "💡 提示：我们当前处于持久化沙盒模式，网络拓扑未被物理强拆。"
echo "🚨 剩余空转损耗提示："
echo "   - 4 枚 Private Endpoints 保持存活，将持续消耗大约 \$0.04/小时。"
echo "   - 建议复工周期不要超过 48 小时，以保障学生订阅的 90 USD 安全水位线。"
echo "========================================================="
echo "🎉 财务防线合拢成功。环境已安全静默挂起！"
echo "========================================================="